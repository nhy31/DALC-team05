package dongduk.dalc05.aah.domain;

public class Media {
	
	private int media_code;
	private String media_title;
	private int media_type;
	private int sick_code;
	
	public int getMedia_code() {
		return media_code;
	}
	public void setMedia_code(int media_code) {
		this.media_code = media_code;
	}
	public String getMedia_title() {
		return media_title;
	}
	public void setMedia_title(String media_title) {
		this.media_title = media_title;
	}
	public int getMedia_type() {
		return media_type;
	}
	public void setMedia_type(int media_type) {
		this.media_type = media_type;
	}
	public int getSick_code() {
		return sick_code;
	}
	public void setSick_code(int sick_code) {
		this.sick_code = sick_code;
	}
	
	


}
